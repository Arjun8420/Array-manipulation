#Array manipulation
import numpy as np

a = np.array([[1,2],
            [3,4]])

# To print the minimum value
print(a.min())

# To print the maximum value
print(a.max())

# To print the Mean value
print(a.mean())

# To print the standard devation value
print(a.std())

# To print the variance value
print(a.var())
